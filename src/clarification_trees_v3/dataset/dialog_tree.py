class NodeType(Enum):
    ROOT = 0  # The initial question and paired image
    CLARIFICATION_QUESTION = 1  # Question generated by clarification model
    CLARIFYING_ANSWER = 2  # Answer generated by human stand-in model
    INFERENCE = 3  # Answer generated by inference model

class DialogNode:
    node_type_to_str = {
        NodeType.ROOT: "user",
        NodeType.CLARIFICATION_QUESTION: "assistant",
        NodeType.CLARIFYING_ANSWER: "user",
        NodeType.INFERENCE: "assistant"
    }
    reverse_role_map = {
        "user": "assistant",
        "assistant": "user"
    }

    def __init__(self, node_type: NodeType, image: Optional[Image.Image], image_path: Optional[Path], response: str,
                 inference_score: float | None = None,
                 inference_scores_raw: list[int] | None = None,
                 question_presence_cost: float | None = None,
                 entailment_cost: float | None = None,
                 reward: float | None = None,
                 infer_reward: float | None = None,
                 defer_reward: float | None = None,
                 advantage: float | None = None,
                 token_logprobs: list[tuple[int, float]] | None = None):
        self.node_type = node_type
        self.image = image
        self.response = response
        self.image_path = image_path
        if self.image_path is None:
            recovered_path = getattr(self.image, "filename", None)
            if recovered_path is not None:
                self.image_path = Path(recovered_path)

        self.inference_score = inference_score
        self.inference_scores_raw = inference_scores_raw
        self.question_presence_cost = question_presence_cost
        self.entailment_cost = entailment_cost
        self.reward = reward
        self.infer_reward = infer_reward
        self.defer_reward = defer_reward
        self.advantage = advantage
        self.token_logprobs = token_logprobs

        self.model_name_to_message_generator = {
            "qwen-3-vl": self._to_qwen_message,
            "qwen-3-vl-2b": self._to_qwen_message,
            "qwen-3-vl-4b": self._to_qwen_message,
            "qwen-3-vl-8b": self._to_qwen_message,
            "qwen-3-vl-32b": self._to_qwen_message,
        }

    def _to_qwen_message(self, reverse_roles: bool = False, use_img_path: bool = False):
        content = []
        if self.image is not None or self.image_path is not None:
            img_content: dict[str, Any] = {"type": "image"}
            if use_img_path:
                # Then we use the image path instead of the image itself as the data
                assert self.image_path is not None, "If using image paths, the image path must be set"
                img_content["image_url"] = {"url": f"file://{self.image_path}"}
                img_content["uuid"] = self.image_path.name
            else:
                image_to_use = self.image
                if image_to_use is None and self.image_path is not None:
                    image_to_use = Image.open(self.image_path).convert('RGB')
                
                assert image_to_use is not None, "If not using image paths, the image must be set or path must be valid"
                img_content["image"] = image_to_use
                if self.image_path is not None:
                    img_content["uuid"] = self.image_path.name
                else:
                    img_content["uuid"] = hashlib.md5(image_to_use.tobytes()).hexdigest()
            content.append(img_content)
        if self.response is not None and len(self.response) > 0:
            content.append({"type": "text", "text": self.response})
        assert len(content) > 0, "DialogNode must have either an image or a response"

        role = self.node_type_to_str[self.node_type]
        if reverse_roles:
            role = self.reverse_role_map[role]

        return {
            "role": role,
            "content": content
        }

    def to_message(self, model_name: str, reverse_roles: bool = False, use_img_path: bool = False):
        return self.model_name_to_message_generator[model_name](reverse_roles, use_img_path)

    def to_string(self):
        string = f"{self.node_type_to_str[self.node_type]}: {self.response}"
        if self.image is not None:
            if self.image_path is not None:
                string += f" (w/ Image: {self.image_path.name})"
            else:
                string += f" (w/ Image: {self.image})"
        return string

    def __repr__(self):
        return self.to_string()

    def to_dict(self):
        return {
            "node_type": self.node_type.value,
            "image_path": self.image_path.absolute().as_posix() if self.image_path is not None else None,
            "response": self.response,
            "inference_score": self.inference_score,
            "inference_scores_raw": self.inference_scores_raw,
            "question_presence_cost": self.question_presence_cost,
            "entailment_cost": self.entailment_cost,
            "reward": self.reward,
            "infer_reward": self.infer_reward,
            "defer_reward": self.defer_reward,
            "advantage": self.advantage,
            "token_logprobs": self.token_logprobs
        }

    @classmethod
    def from_dict(cls, data: dict, load_images: bool = True):
        node_type = NodeType(data["node_type"])
        response = data["response"]
        image_path = data.get("image_path")
        if image_path is not None:
            image_path = Path(image_path)
        image = None
        if image_path is not None and load_images:
            try:
                # Use 'with' to ensure the file descriptor is closed and eagerly load/copy
                with Image.open(image_path) as img:
                    image = img.convert("RGB")
            except (FileNotFoundError, OSError):
                print(f"Warning: Could not reload image at {image_path}")
                image = None
                
        return cls(
            node_type, image, image_path, response,
            inference_score=data.get("inference_score"),
            inference_scores_raw=data.get("inference_scores_raw"),
            question_presence_cost=data.get("question_presence_cost"),
            entailment_cost=data.get("entailment_cost"),
            reward=data.get("reward"),
            infer_reward=data.get("infer_reward"),
            defer_reward=data.get("defer_reward"),
            advantage=data.get("advantage"),
            token_logprobs=[tuple(x) for x in data["token_logprobs"]] if data.get("token_logprobs") else None
        )

class DialogTrajectory:
    trajectory: List[DialogNode]
    
    def __init__(self):
        self.trajectory = []  # List of DialogNode objects

    @staticmethod
    def from_dialog_tree(dialog_tree: 'DialogTree', leaf_node_idx: int):
        trajectory = DialogTrajectory()
        while leaf_node_idx != -1:
            parent_idx, node = dialog_tree.nodes[leaf_node_idx]
            trajectory.trajectory.append(node)
            leaf_node_idx = parent_idx
        return trajectory

    def to_messages(self, model_name: str, reverse_roles: bool = False, use_img_path: bool = False):
        return [node.to_message(model_name, reverse_roles, use_img_path) for node in self.trajectory][::-1]

    def to_string(self):
        string = f"Trajectory of length: {len(self.trajectory)}\n"
        for node in self.trajectory[::-1]:
            string += f"\t{node.to_string()}\n"
        return string

    def __repr__(self):
        return self.to_string()

class DialogTree:
    ROOT = 0

    def __init__(
        self,
        init_question: str, init_image: Image.Image | None = None, init_image_path: Path | None = None,
        init_image_caption: str | None = None,
        unambiguous_question: str | None = None,
        gold_answer: str | None = None, answers: List[str] | None = None
    ):
        # self.init_data = (-1, NodeType.ROOT, init_image, init_question)  # Parent index, Node type, Image, Question
        self.init_data = (-1, DialogNode(NodeType.ROOT, init_image, init_image_path, init_question))
        self.nodes = [self.init_data]
        self.children: dict[int, List[int]] = {0: []}
        self.transition_probs: dict[tuple[int, int], float | None] = {}  # Maps (parent_idx, child_idx) to transition probability

        self.init_question = init_question
        self.init_image = init_image
        self.init_image_path = init_image_path
        self.init_image_caption = init_image_caption
        self.unambiguous_question = unambiguous_question
        self.gold_answer = gold_answer
        self.answers = answers

    def get_parent_idx(self, node_idx: int) -> int:
        return self.nodes[node_idx][0]

    def get_children_idxs(self, parent_idx: int, type_filter: NodeType | None = None) -> List[int]:
        if type_filter is None:
            return self.children[parent_idx]
        else:
            return [child_idx for child_idx in self.children[parent_idx] if self.nodes[child_idx][1].node_type == type_filter]

    def get_node(self, node_idx: int) -> DialogNode:
        return self.nodes[node_idx][1]

    def get_nodes(self, node_type: NodeType | None = None) -> List[tuple[int, DialogNode]]:
        return [(idx, node) for idx, (parent_idx, node) in enumerate(self.nodes) if node_type is None or node.node_type == node_type]

    def add_node(self, parent_idx: int, node_type: NodeType, response: str, image: Optional[Image.Image] = None, image_path: Path | None = None, transition_prob: float | None = None):
        self.nodes.append((parent_idx, DialogNode(node_type, image, image_path, response)))
        added_index = len(self.nodes) - 1
        self.transition_probs[(parent_idx, added_index)] = transition_prob
        self.children[parent_idx].append(added_index)
        self.children[added_index] = []
        return added_index

    def get_trajectory(self, node_idx: int):
        return DialogTrajectory.from_dialog_tree(self, node_idx)

    def save(self, output_path: str | Path):
        """Saves the dialog tree to a JSON file."""
        data = {
            "init_image_caption": self.init_image_caption,
            "unambiguous_question": self.unambiguous_question,
            "gold_answer": self.gold_answer,
            "answers": self.answers,
            # Serialize nodes: list of [parent_index, node_dict]
            "nodes": [
                (parent_idx, node.to_dict()) 
                for parent_idx, node in self.nodes
            ],
            # Serialize children: list of [parent_idx, child_idx]
            "children": self.children,
            # Serialize transitions: list of [parent_idx, child_idx, probability]
            "transition_probs": [
                (p, c, prob) 
                for (p, c), prob in self.transition_probs.items()
            ]
        }

        with open(output_path, 'w') as f:
            json.dump(data, f, indent=4)

    @classmethod
    def load(cls, input_path: str | Path, load_images: bool = True):
        """Loads a dialog tree from a JSON file."""
        with open(input_path, 'r') as f:
            data = json.load(f)

        # Reconstruct the root node to initialize the tree
        root_parent_idx, root_node_data = data["nodes"][0]
        root_node = DialogNode.from_dict(root_node_data, load_images=load_images)
        # assert root_node.image is not None, "Root node must have an image to be loaded"
        
        # Initialize tree with root data
        tree = cls(
            init_question=root_node.response,
            init_image=root_node.image,
            init_image_caption=data.get("init_image_caption"),
            unambiguous_question=data.get("unambiguous_question"),
            gold_answer=data.get("gold_answer"),
            answers=data.get("answers")
        )
        
        # Override the automatically created root node to ensure exact match (though usually identical)
        tree.nodes = [(root_parent_idx, root_node)]

        # Add remaining nodes
        for parent_idx, node_data in data["nodes"][1:]:
            node = DialogNode.from_dict(node_data)
            tree.nodes.append((parent_idx, node))
        tree.children = {int(k): v for k, v in data["children"].items()}

        # Restore transition probabilities
        tree.transition_probs = {
            (p, c): prob 
            for p, c, prob in data["transition_probs"]
        }

        return tree

    def _compute_reward_recursive(self, cur_node_id: int) -> float:
        # Step 0: Check for base case: We are an inference node. If we are not and have no children, this is an error.
        node = self.get_node(cur_node_id)
        if node.node_type == NodeType.INFERENCE:
            node.reward = node.inference_score if node.inference_score is not None else 0.0
            return node.reward

        # Step 1: Compute rewards for all children
        child_node_idxs = self.get_children_idxs(cur_node_id)
        if len(child_node_idxs) == 0:
            raise ValueError(f"Node {cur_node_id} has no children.")
        
        child_rewards = []
        for child_node_idx in child_node_idxs:
            child_rewards.append(self._compute_reward_recursive(child_node_idx))
        
        # Step 2: Compute expected inference score for all children that are INFERENCE nodes
        total_inference_prob: float = 0
        inference_reward: float = 0
        for i, child_idx in enumerate(child_node_idxs):
            if self.get_node(child_idx).node_type != NodeType.INFERENCE:
                continue

            transition_prob = self.transition_probs.get((cur_node_id, child_idx))
            assert transition_prob is not None, f"Probability of transition from {cur_node_id} to {child_idx} is None."
            child_reward = child_rewards[i]
            inference_reward += transition_prob * child_reward
            total_inference_prob += transition_prob
        assert np.isclose(total_inference_prob, 1) or total_inference_prob == 0, f"Total inference probability is {total_inference_prob}. It must be either 0 or 1."
        if total_inference_prob == 0:
            inference_reward = -np.inf
        
        # Step 3: Compute expected reward for all children that are not INFERENCE nodes
        total_deferral_prob: float = 0
        deferral_reward: float = 0
        for i, child_idx in enumerate(child_node_idxs):
            if self.get_node(child_idx).node_type == NodeType.INFERENCE:
                continue

            transition_prob = self.transition_probs.get((cur_node_id, child_idx))
            assert transition_prob is not None, f"Probability of transition from {cur_node_id} to {child_idx} is None."
            child_reward = child_rewards[i]
            deferral_reward += transition_prob * child_reward
            total_deferral_prob += transition_prob
        assert np.isclose(total_deferral_prob, 1) or total_deferral_prob == 0, f"Total deferral probability is {total_deferral_prob}. It must be either 0 or 1."
        if total_deferral_prob == 0:
            deferral_reward = -np.inf

        # Apply costs (Costs are given as negative values, so we add them)
        if node.question_presence_cost is not None:
            deferral_reward += node.question_presence_cost
        if node.entailment_cost is not None:
            deferral_reward += node.entailment_cost
        
        # Step 5: Compute reward for current node
        reward = max(inference_reward, deferral_reward)
        node.infer_reward = inference_reward
        node.defer_reward = deferral_reward
        node.reward = reward
        return reward

    def _compute_advantage(self):
        all_rewards = [node.reward for idx, node in self.nodes if node.reward is not None]
        if not all_rewards:
            return
            
        std = np.std(all_rewards)
        if std == 0:
            std = 1.0  # Avoid division by zero

        seen_parent_ids = set()
        for node_idx, node in self.get_nodes(NodeType.CLARIFICATION_QUESTION):
            parent_id = self.get_parent_idx(node_idx)
            if parent_id in seen_parent_ids:
                continue
            seen_parent_ids.add(parent_id)

            children = self.get_children_idxs(parent_id, type_filter=NodeType.CLARIFICATION_QUESTION)
            valid_rewards = [self.get_node(child).reward for child in children if self.get_node(child).reward is not None]
            if not valid_rewards:
                continue
                
            mean = np.mean(valid_rewards)

            for child_idx in children:
                child_node = self.get_node(child_idx)
                if child_node.reward is not None:
                    advantage = (child_node.reward - mean) / std
                    child_node.advantage = float(advantage)

    def compute_rewards(self):
        self._compute_reward_recursive(self.ROOT)
        self._compute_advantage()

    def get_node_advantage(self, node_idx: int) -> float:
        node = self.get_node(node_idx)
        if node.advantage is None:
            self.compute_rewards()
        return node.advantage


def visualize_tree(dialog_tree: DialogTree, output_filename: str = "dialog_tree", view: bool = True):
    """
    Generates a visual flow diagram of the DialogTree using Graphviz.
    
    Args:
        dialog_tree: The DialogTree instance.
        output_filename: The name of the file to save (without extension).
        view: Whether to open the rendered file immediately.
    """
    
    # Create the directed graph
    dot = Digraph(name='DialogTree', comment='Dialog Tree Visualization')
    dot.attr(rankdir='LR')  # Top to Bottom layout
    dot.attr('node', shape='plaintext') # Use plaintext so our HTML tables define the shape
    
    # distinct colors for roles
    colors = {
        NodeType.ROOT: "#E0F7FA",                  # Cyan-ish (User)
        NodeType.CLARIFICATION_QUESTION: "#FFF3E0",# Orange-ish (Assistant)
        NodeType.CLARIFYING_ANSWER: "#E0F7FA",     # Cyan-ish (User)
        NodeType.INFERENCE: "#E8F5E9"              # Green-ish (Final Answer)
    }

    # Helper to handle image paths for graphviz
    temp_dir = tempfile.mkdtemp()

    # Cache the rewards
    dialog_tree.compute_rewards()
    
    for idx, (parent_idx, node) in enumerate(dialog_tree.nodes):
        
        # 1. Prepare Image HTML
        img_html = ""
        if node.image is not None:
            # We need a physical path for Graphviz to load the image.
            # If image_path exists, use it; otherwise save a temp file from PIL.
            if node.image_path and node.image_path.exists():
                img_fpath = node.image_path.absolute().as_posix()
            else:
                # Save temp image from PIL
                img_fpath = os.path.join(temp_dir, f"node_{idx}.png")
                node.image.save(img_fpath)
            
            # Scale image to fit nicely in the box
            img_html = f'<tr><td><img src="{img_fpath}" scale="true" width="150"/></td></tr>'

        # # 2. Prepare Text Content
        # # Truncate long responses for readability
        # display_text = node.response
        # if len(display_text) > 100:
        #     display_text = display_text[:97] + "..."
        display_text = textwrap.fill(node.response, width=50)
        
        # Escape HTML special characters in text
        display_text = display_text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
        display_text = display_text.replace("\n", "<br/>")

        # Get node sidecar metadata
        meta_text = None
        reward_text = None

        if node.node_type == NodeType.INFERENCE:
            inference_score = node.inference_score
            if inference_score is not None:
                meta_text = f"Inference Score: {inference_score:.2f}"
            else:
                meta_text = "Inference Score: Not Found"
        elif node.node_type == NodeType.CLARIFICATION_QUESTION:
            question_presence_score = node.question_presence_cost
            entailment_score = node.entailment_cost
            if question_presence_score is not None:
                meta_text = f"Question Presence Score: {question_presence_score:.2f}"
            else:
                meta_text = "Question Presence Score: Not Found"
            if entailment_score is not None:
                meta_text += f"<BR/>Entailment Score: {entailment_score:.2f}"
            else:
                meta_text += "<BR/>Entailment Score: Not Found"
        
        if node.reward is not None:
            reward = node.reward
            infer_reward = node.infer_reward
            if infer_reward is not None and infer_reward != float('-inf'):
                infer_reward = f"{infer_reward:.2f}"
            else:
                infer_reward = "N/A"
            defer_reward = node.defer_reward
            if defer_reward is not None and defer_reward != float('-inf'):
                defer_reward = f"{defer_reward:.2f}"
            else:
                defer_reward = "N/A"
            reward_text = f"Reward: {reward:.2f} (Infer: {infer_reward}, Defer: {defer_reward})"

        if node.advantage is not None:
            advantage = node.advantage
            if reward_text is None:
                reward_text = ""
            else:
                reward_text += "<BR/>"
            reward_text += f"Advantage: {advantage:.2f}"
                

        # 3. Construct Node Label (HTML-like)
        header_color = "#333333"
        bg_color = colors.get(node.node_type, "#FFFFFF")
        meta_color = "#eeeeee"

        match node.node_type:
            case NodeType.INFERENCE:
                node_type_name = "Inference"
                additional_rows = ""
            case NodeType.CLARIFICATION_QUESTION:
                node_type_name = "Clarification Question"
                additional_rows = ""
            case NodeType.CLARIFYING_ANSWER:
                node_type_name = "Clarifying Answer"
                additional_rows = ""
            case NodeType.ROOT:
                node_type_name = "Root"
                if dialog_tree.unambiguous_question is not None and dialog_tree.gold_answer is not None:
                    additional_rows = f"""
                    <tr><td bgcolor="{meta_color}">Orig Q: {textwrap.fill(dialog_tree.unambiguous_question, width=50).replace("\n", "<br/>")}</td></tr>
                    <tr><td bgcolor="{meta_color}">Gold A: {dialog_tree.gold_answer}</td></tr>
                    <tr><td bgcolor="{meta_color}">All As: {dialog_tree.answers}</td></tr>
                    """
                else:
                    additional_rows = ""
            case _:
                raise ValueError(f"Unknown node type: {node.node_type}")
        
        label = f'''<<table border="0" cellborder="1" cellspacing="0" cellpadding="4" bgcolor="{bg_color}">
            <tr><td bgcolor="{header_color}"><font color="white"><b>{node_type_name}</b></font></td></tr>
            {img_html}
            <tr><td>{display_text}</td></tr>
            {f"<tr><td bgcolor=\"{meta_color}\">{meta_text}</td></tr>" if meta_text is not None else ""}
            {f"<tr><td bgcolor=\"{meta_color}\">{reward_text}</td></tr>" if reward_text is not None else ""}
            {additional_rows}
        </table>>'''

        dot.node(str(idx), label=label)

        # 4. Create Edges
        if parent_idx != -1:
            # Check for transition probability
            prob = dialog_tree.transition_probs.get((parent_idx, idx))
            edge_label = f"{prob:.2f}" if prob is not None else ""
            dot.edge(str(parent_idx), str(idx), label=edge_label)

    # Render
    try:
        output_path = dot.render(output_filename, format='png', view=view)
        print(f"Visualization saved to: {output_path}")
        return output_path
    except Exception as e:
        print(f"Error rendering Graphviz: {e}")
        print("Ensure 'graphviz' is installed on your system (e.g., 'brew install graphviz' or 'apt-get install graphviz')")


